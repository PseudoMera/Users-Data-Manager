export class User {
    constructor(name, surname, lastname, identification, age, gender, address, phone, email, civilStatus, hasChilds, birthday) {
        this.name = name;
        this.surname = surname;
        this.lastname = lastname;
        this.identification = identification;
        this.age = age;
        this.gender = gender;
        this.address = address;
        this.phone = phone;
        this.email = email;
        this.civilStatus = civilStatus;
        this.hasChilds = hasChilds;
        this.birthday = birthday;  
    }
}